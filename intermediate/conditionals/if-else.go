package main

import "fmt"

func main() {
	// Declare a boolean variable 'isHungry' and set it to false
	isHungry := false

	// if-else block to check the value of 'isHungry'
	if isHungry {
		// If 'isHungry' is true, this line will be executed
		fmt.Println("Eat the cookie")
	} else {
		// If 'isHungry' is false, this line will be executed
		fmt.Println("Step away from the cookie...")
	}
}
